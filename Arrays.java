public class Arrays {
    public static void main(String[] args) {
        int[] marks={23,34,45,56,67};
        for(int i=0;i<marks.length;i++){
            System.out.println(marks[i]);
        }
        for (int i=4;i<marks.length;i--){
            System.out.println(marks[i]);
        }

    }
    
}
